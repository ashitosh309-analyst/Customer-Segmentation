DROP TABLE IF EXISTS rfm_table;
CREATE TABLE rfm_table AS 
WITH rfm AS (
    SELECT 
        CUSTOMERNAME, 
        SUM(SALES) AS MonetaryValue,
        AVG(SALES) AS AvgMonetaryValue,
        COUNT(ORDERNUMBER) AS Frequency,
        -- Convert string to actual Date objects for math
        MAX(STR_TO_DATE(ORDERDATE, '%d/%m/%Y')) AS LastOrderDate,
        (SELECT MAX(STR_TO_DATE(ORDERDATE, '%d/%m/%Y')) FROM sales) AS MaxOrderDate,
        -- Calculate Recency using valid Date objects
        DATEDIFF(
            (SELECT MAX(STR_TO_DATE(ORDERDATE, '%d/%m/%Y')) FROM sales), 
            MAX(STR_TO_DATE(ORDERDATE, '%d/%m/%Y'))
        ) AS Recency
    FROM sales
    GROUP BY CUSTOMERNAME
),
rfm_calc AS (
    SELECT *,
        NTILE(4) OVER (ORDER BY Recency DESC) rfm_recency,
        NTILE(4) OVER (ORDER BY Frequency) rfm_frequency,
        NTILE(4) OVER (ORDER BY MonetaryValue) rfm_monetary
    FROM rfm
)
SELECT *, 
    (rfm_recency + rfm_frequency + rfm_monetary) AS rfm_cell,
    CONCAT(rfm_recency, rfm_frequency, rfm_monetary) AS rfm_cell_string
FROM rfm_calc;

